"""Метрика «казахскости» ответа.

System prompt KazGPT требует: «ӘРДАЙЫМ қазақ тілінде жауап бер».
Эта метрика проверяет, насколько модель этому следует.

Принцип:
1. Разбиваем ответ на слова.
2. Каждое слово классифицируем как казахское/русское/английское/другое.
3. Возвращаем долю казахских слов от total.

Эвристики (без тяжёлого NLP):
- Казахские специфические буквы: ә, ө, ү, ұ, қ, ғ, ң, һ, і → слово казахское
- Чисто кириллица без казахских букв и без типичных русских окончаний → может быть казахским
- Латиница → английский
- Числа и пунктуация в подсчёт не входят
"""

import re
from typing import Dict

# Уникальные казахские буквы (отсутствуют в русском)
KZ_SPECIFIC = set("әөүұқғңһі")

# Расширенный список часто встречающихся русских слов (function words + bigram-ы).
# Если слово в этом наборе — однозначно русский.
RU_WORDS = {
    "привет", "пока", "здравствуй", "здравствуйте",
    "как", "что", "где", "когда", "почему", "зачем", "кто",
    "это", "этот", "эта", "эти", "тот", "та", "те",
    "который", "которая", "которое", "которые",
    "если", "потому", "также", "тоже", "просто", "только", "уже",
    "нужно", "можно", "нельзя", "должен", "должна", "должно",
    "сейчас", "сегодня", "завтра", "вчера", "всегда", "никогда",
    "очень", "много", "мало", "немного", "ещё", "еще",
    "хорошо", "плохо", "правильно",
    "дела", "спасибо", "пожалуйста", "извини", "извините",
    "мне", "тебе", "ему", "ей", "нам", "вам", "им",
    "меня", "тебя", "его", "ее", "её", "нас", "вас", "их",
    "я", "ты", "он", "она", "оно", "мы", "вы", "они",
    "не", "ни", "ну", "да", "нет", "может", "будет", "был", "была", "были",
    "для", "без", "над", "под", "при", "про", "через", "из-за",
}

# Типичные русские окончания/суффиксы — если слово их содержит, скорее всего русский.
RU_SUFFIX = re.compile(
    r"(?:ться|тся|ется|ишь|ешь|ое|ая|ую|ого|ему|ыми|ами|ями|"
    r"ность|ение|ания|ство|чик|щик)$",
    re.IGNORECASE,
)


def _classify_word(word: str) -> str:
    """Возвращает один из: 'kz', 'ru', 'en', 'other'."""
    w = word.lower()
    if not w:
        return "other"

    # Латиница
    if re.fullmatch(r"[a-zA-Z\-']+", w):
        return "en"

    # Есть казахская специфическая буква → однозначно казахский
    if any(ch in KZ_SPECIFIC for ch in w):
        return "kz"

    # Кириллица без kz-букв: смотрим маркеры русского.
    if w in RU_WORDS:
        return "ru"
    if RU_SUFFIX.search(w):
        return "ru"

    # Остаток — кириллица без явных RU-маркеров. Это могут быть:
    #   - казахские термины без спец-букв («Алматы», «Астана», «бар», «жоқ»)
    #   - русские имена собственные («Москва») — редкость в KazGPT
    # Засчитываем как kz (нашей модели на пользу), но это место для улучшения через
    # казахский wordlist если метрика окажется слишком оптимистичной.
    if re.fullmatch(r"[Ѐ-ӿ\-']+", w):
        return "kz"

    return "other"


def kz_purity(text: str) -> Dict:
    """Возвращает долю казахских слов и breakdown по языкам.

    Returns:
        dict с полями:
          - purity: float в [0, 1] — доля kz-слов
          - kz, ru, en, other: счётчики
          - total: общее число слов
    """
    words = re.findall(r"[\wЀ-ӿ\-']+", text)
    if not words:
        return {"purity": 0.0, "kz": 0, "ru": 0, "en": 0, "other": 0, "total": 0}

    counts = {"kz": 0, "ru": 0, "en": 0, "other": 0}
    for w in words:
        counts[_classify_word(w)] += 1

    total = len(words)
    purity = counts["kz"] / total if total else 0.0
    return {
        "purity": round(purity, 3),
        "kz": counts["kz"],
        "ru": counts["ru"],
        "en": counts["en"],
        "other": counts["other"],
        "total": total,
    }


if __name__ == "__main__":
    cases = [
        "Сәлеметсіз! Мен KazGPT көмекшісімін.",
        "Привет, как дела?",
        "Hello, my name is KazGPT.",
        "Алматы — Қазақстанның оңтүстігіндегі қала. Это intersting.",
        "",
    ]
    for c in cases:
        r = kz_purity(c)
        print(f"[{c[:40]!r:<45}] purity={r['purity']} ({r['kz']}/{r['total']}) ru={r['ru']} en={r['en']}")
